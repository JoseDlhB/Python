# Script que suma 4 valores

print("ingrese 4 valores:\n")
valor1 = float(input(("valor 1:")))
valor2 = float(input(("valor 2:")))
valor3 = float(input(("valor 3:")))
valor4 = float(input(("valor 4:")))

suma = valor1 + valor2 + valor3 + valor4

print(f"({valor1})+({valor2})+({valor3})+({valor4}) = {suma}")
